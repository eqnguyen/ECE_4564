<?php
/****************************************************************************************************** */
/* RASDRIVE SITE CONTROLLER */
/****************************************************************************************************** */
include_once '../global.php';

// get the identifier for the page we want to load
$action = $_GET['action'];

// instantiate a SiteController and route it
$sc = new SiteController();
$sc->route($action);

$page = null;

class SiteController {

	// route us to the appropriate class method for this action
	public function route($action) {
		switch($action) {
			/************************************************************** */
			/* HOME PAGE */
			/************************************************************** */
			case 'home':
			$this->home();
			break;

			/************************************************************** */
			/* BACKUP PAGE */
			/************************************************************** */
			case 'backup':
			$this->backup();
			break;

			/************************************************************** */
			/* SYNC PAGE */
			/************************************************************** */
			case 'sync':
			$this->sync();
			break;

			/************************************************************** */
			/* STATUS PAGE */
			/************************************************************** */
			case 'status':
			$this->status();
			break;

			/************************************************************** */
			/* ABOUT PAGE */
			/************************************************************** */
			case 'about':
			$this->about();
			break;
		}    
	}

	/******************************************************************************************* */
	/* HOME PAGE */
	/******************************************************************************************* */
	public function home() {
		$page = 'Home';
		include_once SYSTEM_PATH.'/view/home.tpl';

	}

	/******************************************************************************************* */
	/* BACKUP PAGE */
	/******************************************************************************************* */
	public function backup() {
		$page = 'Backup';
		include_once SYSTEM_PATH.'/view/backup.tpl';

	}

	/******************************************************************************************* */
	/* SYNC PAGE */
	/******************************************************************************************* */
	public function sync() {
		$page = 'Sync';
		include_once SYSTEM_PATH.'/view/sync.tpl';

	}

	/******************************************************************************************* */
	/* STATUS PAGE */
	/******************************************************************************************* */
	public function status() {
		$page = 'Status';
		include_once SYSTEM_PATH.'/view/status.tpl';

	}

	/******************************************************************************************* */
	/* ABOUT PAGE */
	/******************************************************************************************* */
	public function about() {
		$page = 'About';
		include_once SYSTEM_PATH.'/view/about.tpl';

	}

	

} //end site controller class


